package example;
public class MenuItem {
        private String name;
        private int price;
        private int id;

        public MenuItem(String name, int price, int id) {
            this.name = name;
            this.price = price;
            this.id = id;
        }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // Getter 및 Setter 메서드 생략
    }


